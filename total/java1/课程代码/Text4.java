public class Text4 {
    public static void main(String[] args) {
        float a = 3.14F;//声明float类型变量，必须在值后面加个F或f
        double b = 3.14;//声明double类型变量，可以直接写值
        double c = 3.14D;//声明double类型变量，也可以在值后面加个D

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
    }
}