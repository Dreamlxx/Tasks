//三目运算符
public class Text6 {
    public static void main(String[] args) {
        int a = 60;
        int b = 59;
        System.out.println(a >= 60 ? "pass" : "no pass");
        System.out.println(b >= 60 ? "pass" : "no pass");
        String s = a >= 60 ? "pass" : "no pass";//可以直接复制给字符串欸
        System.out.println(s);
    }
}
